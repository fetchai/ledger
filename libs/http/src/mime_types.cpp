MimeType GetMimeTypeFromExtension(std::string const &ext)
{
  MimeType mime({ext, "application/octet-stream"});

  auto first = types.begin();
  first      = std::lower_bound(first, types.end(), mime);
  if (first->extension == ext)
  {
    return *first;
  }
  return mime;
}

